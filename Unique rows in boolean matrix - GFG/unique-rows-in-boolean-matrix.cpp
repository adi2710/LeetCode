class Solution
{
    public:
    // #define MAX 1000
    vector<vector<int>> uniqueRow(int M[MAX][MAX],int row,int col)
    {
        //Your code here
        vector<vector<int>> ans;
        unordered_set<string> st;
        for(int r = 0; r < row; r++)
        {
            vector<int> v(col);
            string temp;
            for(int c = 0; c < col; c++)
            {
                temp += to_string(M[r][c]);
                v[c] = M[r][c];
            }
            if(st.find(temp) == st.end())
            {
                st.insert(temp);
                ans.push_back(v);
            }
        }
        return ans;
    }
};

