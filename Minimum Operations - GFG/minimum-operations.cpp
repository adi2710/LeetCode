class Solution
{
  public:
    int minOperation(int n)
    {
        //code here.
        int msb = __builtin_clz(n), ans = 0;
        while(n)
        {
            ans++;
            n &= (n - 1);
        }
        return ans + (31 - msb);
    }
};

